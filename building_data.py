    currency_symbols = {
        "gold": "üü°",
        "elixir": "üíú", 
        "dark_elixir": "‚ö´"
    }
    
    if amount >= 1000000:
        return f"{amount // 1000000}–ú {currency_symbols.get(currency, '')}"
